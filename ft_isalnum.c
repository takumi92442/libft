#include "libft.h"
int ft_isalnum(int c)
{
    if((c <= 'Z' && c >= 'A')||(c>='a'&&c<='z')||(c >= 48 && c <= 57))
    {
        return (1);
    }
    else
    {
        return (0);
    }
}
